#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		len;

	len = ft_strlen(s1);
	if (!(s2 = ft_strnew(len)))
		return (0);
	s2 = (char *)ft_memcpy(s2, s1, len);
	return (s2);
}
