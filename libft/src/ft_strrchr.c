#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*str;
	int		len;

	len = ft_strlen(s) + 1;
	str = (char *)s;
	while (*str)
		str++;
	while (len--)
	{
		if (*str == c)
			return (str);
		str--;
	}
	return (NULL);
}
