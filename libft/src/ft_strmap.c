#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *ns;
	char *tmp;

	ns = NULL;
	tmp = NULL;
	if (s && f)
	{
		if (!(ns = ft_strnew(ft_strlen(s))))
			return (NULL);
		tmp = ns;
		while (*s)
			*ns++ = f(*s++);
	}
	return (tmp);
}
