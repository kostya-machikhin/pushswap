#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	ft_memcpy((void *)dst, (const void *)src, len);
	if (len > ft_strlen(src))
		ft_bzero((void *)(dst + ft_strlen(src)), len - ft_strlen(src));
	return (dst);
}
